package com.mercadolibre.challenge.quasar.model;

/**
 * Clase para representar una posicion
 * @author omarnl
 *
 */
public class Position {
	
    private double x;

    private  double y;

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

    
}
